//--------------------------------------------------------------
// 更新
//--------------------------------------------------------------
void CItemData::Update()
{
	ItemState_();
}

//--------------------------------------------------------------
// 生成
//--------------------------------------------------------------
CItemData* CItemData::Create(int inId)
{
	CItemData* pItemData = nullptr;
	pItemData = new CItemData;

	if (pItemData != nullptr)
	{
		pItemData->Init();
		pItemData->SetItemData((ITEM_DATA)inId);
	}

	return pItemData;
}

