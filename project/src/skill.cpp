//--------------------------------------------------------------
// 更新処理
//--------------------------------------------------------------
void CSkill::Update(void)
{
	// スキルデータのインスタンスを取得する
	CSkillDataBase *pSkillData = CSkillDataBase::GetInstance();

	if (m_CT > 0)
	{// クールタイムがあれば減少させる
		m_CT--;
	}
	
	m_atkSpd = m_apChara->GetAtkSpd()->GetCurrent();

#ifdef _DEBUG
	CDebugProc::Print("%sのクールタイム : %d\n", m_Name.c_str(),m_CT);
#endif // _DEBUG
}

//--------------------------------------------------------------
// スキル生成処理
//--------------------------------------------------------------
CSkill *CSkill::Create()
{
	// 生成処理
	CSkill* pSkill = new CSkill;
	pSkill->Init();

	return pSkill;
}

//--------------------------------------------------------------
// スキル1
//--------------------------------------------------------------
void CSkill::Skill1()
{
	if (m_CT == 0)
	{// クールタイムがなければ当たり判定を生成する
		// 当たり判定の持続時間の管理
		CSkillDataBase *pSkillData = CSkillDataBase::GetInstance();
		pSkillData->GetDuration(m_Name);
		pSkillData->GetAbility(m_Name)(m_apChara);

		// クールタイムの設定
		m_CT = pSkillData->GetCT(m_Name) * m_atkSpd;
	}
}

//--------------------------------------------------------------
// スキル1
//--------------------------------------------------------------
void CSkill::Skill2()
{
	if (m_CT == 0)
	{// クールタイムがなければ当たり判定を生成する
	 // 当たり判定の持続時間の管理
		CSkillDataBase *pSkillData = CSkillDataBase::GetInstance();
		pSkillData->GetDuration(m_Name);
		pSkillData->GetAbility(m_Name)(m_apChara);

		// クールタイムの設定
		m_CT = pSkillData->GetCT(m_Name);
	}
}

