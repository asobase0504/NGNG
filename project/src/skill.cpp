CSkill::CSkill(int nPriority)
{

}

//--------------------------------------------------------------
// デストラクタ
//--------------------------------------------------------------
CSkill::~CSkill()
{

}

//--------------------------------------------------------------
// 初期化処理
//--------------------------------------------------------------
HRESULT CSkill::Init()
{
	for (int nCnt = 0; nCnt < MAX_SKILL; nCnt++)
	{
		m_CT[nCnt] = 0;
	}

	return S_OK;
}

//--------------------------------------------------------------
// 終了処理
//--------------------------------------------------------------
void CSkill::Uninit(void)
{
	
}

//--------------------------------------------------------------
// 更新処理
//--------------------------------------------------------------
void CSkill::Update(void)
{
	for (int nCnt = 0; nCnt < MAX_SKILL; nCnt++)
	{
		if (m_CT[nCnt] > 0)
		{
			m_CT[nCnt]--;
		}
	}
}