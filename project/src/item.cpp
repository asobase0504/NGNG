CItem::~CItem()
{
}

//--------------------------------------------------------------
// 初期化
//--------------------------------------------------------------
HRESULT CItem::Init()
{
	return S_OK;
}

//--------------------------------------------------------------
// 終了
//--------------------------------------------------------------
void CItem::Uninit()
{
}

//--------------------------------------------------------------
// 更新
//--------------------------------------------------------------
void CItem::Update()
{
	ItemState_();
}

//--------------------------------------------------------------
// 生成
//--------------------------------------------------------------
CItem* CItem::Create(ITEM_TYPE inId)
{
	CItem* pItemData = nullptr;
	pItemData = new CItem;

	if (pItemData != nullptr)
	{
		pItemData->Init();
		pItemData->SetItemData(inId);
	}

	return pItemData;
}

//--------------------------------------------------------------
// アイテムの効果
//--------------------------------------------------------------
void CItem::ItemState_()
{
	switch (m_itemData)
	{
	case ITEM_POWER_UP:
		break;
	default:
		break;
	}
}
